/* global moment:false, Chart:false, Sparkline:false */

document.addEventListener('DOMContentLoaded', function() {
  /* Chart.js Charts */
  // Sales chart
  let salesChartCanvas = document.getElementById('revenue-chart-canvas').getContext('2d')

  let chart = $('#revenue-chart').data('chart');

  let salesChartData = {
    labels: chart['months'],
    datasets: [
      {
        label: '',
        backgroundColor: 'rgba(60,141,188,0.9)',
        borderColor: 'rgba(60,141,188,0.8)',
        pointRadius: false,
        pointColor: '#3b8bba',
        pointStrokeColor: 'rgba(60,141,188,1)',
        pointHighlightFill: '#fff',
        pointHighlightStroke: 'rgba(60,141,188,1)',
        data: chart['count'],
      },
    ]
  }

  let salesChartOptions = {
    maintainAspectRatio: false,
    responsive: true,
    legend: {
      display: false
    },
    scales: {
      xAxes: [{
        gridLines: {
          display: false
        }
      }],
      yAxes: [{
        gridLines: {
          display: false
        }
      }]
    }
  }

  // This will get the first returned node in the jQuery collection.
  // eslint-disable-next-line no-unused-vars
  let salesChart = new Chart(salesChartCanvas, { // lgtm[js/unused-local-variable]
    type: 'line',
    data: salesChartData,
    options: salesChartOptions
  });
}, false);
